class AppSpacings {
  /// The default unit of spacing
  static const double defaultSpacingUnit = 16;

  /// xxxs spacing value (1lp)
  static const double xxxs = 0.0625 * defaultSpacingUnit;

  /// xxs spacing value (2lp)
  static const double xxs = 0.125 * defaultSpacingUnit;

  /// xs spacing value (4lp)
  static const double xs = 0.25 * defaultSpacingUnit;

  /// sm spacing value (8lp)
  static const double xm = 0.5 * defaultSpacingUnit;

  /// md spacing value (16lp)
  static const double m =  defaultSpacingUnit;

  /// lg spacing value (20lp)
  static const double l = 1.25 * defaultSpacingUnit;

  /// xlg spacing value (24lp)
  static const double xl = 1.5 * defaultSpacingUnit;

  /// xxlg spacing value (30lp)
  static const double xxl = 1.875 * defaultSpacingUnit;

  /// xxxlg pacing value (42lp)
  static const double xxxl = 2.625 * defaultSpacingUnit;

  /// xxxlg pacing value (60lp)
  static const double xxxxl = 3.75 * defaultSpacingUnit;
}